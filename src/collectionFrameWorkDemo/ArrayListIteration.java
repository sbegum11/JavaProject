package collectionFrameWorkDemo;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

public class ArrayListIteration {
    public static void main(String[] args) {
        //Create and initializing the ArrayList
        //Declering object of integer type
        List<Integer> number = Arrays.asList(1, 2, 3, 4, 5);
        System.out.println(number);

        // Iterating using for loop, we can iterate the ArrayList by using for loop.

        for (int i = 0; i < number.size(); i++)
            System.out.println(number.get(i) + " ");// Printing and display the elements in ArrayList
    }






    }













